					</div>
				</div>
			</div>

			<div class="section">
				<h3>SQL Dialect</h3>
				<select bind:value={selectedDialect} class="dialect-select">
					<option value={SQLDialect.PostgreSQL}>PostgreSQL</option>
					<option value={SQLDialect.MySQL}>MySQL</option>
					<option value={SQLDialect.SQLite}>SQLite</option>
					<option value={SQLDialect.SQLServer}>SQL Server</option>
				</select>
			</div>

			<div class="section">
				<h3>Export Actions</h3>
				<button class="action-btn primary" onclick={downloadSQL}>
					<span class="icon">‚¨á</span>
					Download SQL
				</button>
				<button class="action-btn" onclick={copyToClipboard}>
					<span class="icon">{copied ? '‚úì' : 'üìã'}</span>
					{copied ? 'Copied!' : 'Copy to Clipboard'}
				</button>
			</div>
		</div>

		<div class="main">
			<div class="sql-preview">
				<div class="preview-header">
					<h3>Generated SQL</h3>
					<span class="dialect-badge">
						{selectedDialect}
						{#if isGenerating}
							<span class="generating">‚ü≥</span>
						{/if}
					</span>
				</div>
				{#key sqlKey}
					<pre class="sql-code"><code class="language-sql">{generatedSQL}</code></pre>
				{/key}
			</div>
		</div>
	</div>
</div>

