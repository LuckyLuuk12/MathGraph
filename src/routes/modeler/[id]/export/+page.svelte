<script lang="ts">
	import { page } from '$app/stores';
	import { onMount, untrack } from 'svelte';
	import { goto } from '$app/navigation';
	import { browser } from '$app/environment';
	import { projectStore } from '$lib/stores/project-store';
	import { canvasStore } from '$lib/stores/canvas-store';
	import type { Project } from '$lib/stores/project-store';
	import type { SQLTable } from '$lib/types';
	import { SQLDialect } from '$lib/constants';
	import { SQLGenerator } from '$lib/sql-generator';
	import { SchemaConverter } from '$lib/schema-converter';
	import LoadingScreen from '$lib/components/LoadingScreen.svelte';

	let projectId = $state('');
	let project: Project | null = null;
	let projectName = $state('');
	let selectedDialect = $state<SQLDialect>(SQLDialect.PostgreSQL);
	let generatedSQL = $state('');
	let copied = $state(false);
	let isLoading = $state(true);
	let isGenerating = $state(false);
	let sqlKey = $state(0); // Key to force re-render
	let lastDialect = $state<SQLDialect | null>(null); // Track last dialect to prevent loops

	// Schema statistics
	let schemaStats = $state({
		entities: 0,
		factTypes: 0,
		edges: 0,
		constraints: 0,
		tables: 0,
		relationships: 0
	});

	onMount(() => {
		projectId = $page.params.id || '';
		if (projectId) {
			loadProject();
		} else {
			goto('/projects');
		}
	});

	// Regenerate SQL when dialect changes
	$effect(() => {
		// Track the dialect as a primitive value to ensure reactivity
		const currentDialect = selectedDialect;

		// Only regenerate if dialect actually changed and project is loaded
		if (untrack(() => project !== null && currentDialect !== lastDialect)) {
			lastDialect = currentDialect;
			generateSQL();
		}
	});

	function loadProject() {
		isLoading = true;
		try {
			const allProjects = projectStore.getAllProjects();
			project = allProjects.find((p) => p.id === projectId) || null;

			if (project) {
				projectName = project.name;
				canvasStore.loadState(project.canvasState);
				lastDialect = selectedDialect; // Initialize lastDialect
				generateSQL();
				// Delay to ensure everything is loaded
				setTimeout(() => {
					isLoading = false;
				}, 150);
			} else {
				// Project not found, redirect to projects page
				isLoading = false;
				goto('/projects');
			}
		} catch (error) {
			console.error('Failed to load project:', error);
			isLoading = false;
			goto('/projects');
		}
	}

	function generateSQL() {
		isGenerating = true;
		sqlKey++; // Force re-render of the code block
		generatedSQL = '-- Generating SQL schema...\n';
		// Use setTimeout to allow UI to update before heavy computation
		setTimeout(() => {
			try {
				// Convert canvas state to Information Schema
				const informationSchema = SchemaConverter.convertToInformationSchema(
					$canvasStore,
					projectName
				);

				// Generate SQL using the proper SQL generator
				const generator = new SQLGenerator(selectedDialect);
				const sqlSchema = generator.generateSchema(informationSchema);
				const ddl = generator.generateDDL(sqlSchema);

				// Update statistics
				schemaStats = {
					entities: informationSchema.entities.size,
					factTypes: informationSchema.factTypes.size,
					edges: $canvasStore.edges.size,
					constraints: $canvasStore.constraints.size,
					tables: sqlSchema.tables.length,
					relationships: sqlSchema.tables.reduce(
						(count: number, table: SQLTable) => count + table.foreignKeys.length,
						0
					)
				};

				// Add header comments
				let sql = `-- ============================================================\n`;
				sql += `-- Generated by MathGraph - Visual Information Modeling Tool\n`;
				sql += `-- https://mathgraph.kablan.nl\n`;
				sql += `-- Created by Luuk Kablan\n`;
				sql += `-- ============================================================\n`;
				sql += `--\n`;
				sql += `-- Project: ${projectName}\n`;
				sql += `-- Dialect: ${selectedDialect}\n`;
				sql += `-- Generated: ${new Date().toISOString()}\n`;
				sql += `--\n`;
				sql += `-- This schema was generated from an Information Schema model\n`;
				sql += `-- using formal information systems theory.\n`;
				sql += `--\n`;
				sql += `-- Mapping Rules Applied:\n`;
				sql += `-- - Entities/Objectified Facts ‚Üí Tables\n`;
				sql += `-- - Uniqueness Constraints     ‚Üí PRIMARY KEY / UNIQUE\n`;
				sql += `-- - Total Role Constraints     ‚Üí NOT NULL\n`;
				sql += `-- - Specialization             ‚Üí Foreign Keys\n`;
				sql += `-- - Unary Facts                ‚Üí Boolean Columns\n`;
				sql += `-- - Enumerations               ‚Üí CHECK Constraints\n`;
				sql += `-- - Many-to-Many               ‚Üí Junction Tables\n`;
				sql += `--\n`;
				sql += `-- ============================================================\n\n`;

				if (ddl.trim()) {
					sql += ddl;
				} else {
					sql += '-- No schema elements found to generate SQL.\n';
					sql += '-- Add entities and fact types to your model.\n';
				}

				generatedSQL = sql;

				// Apply syntax highlighting
				if (browser && (window as any).Prism) {
					setTimeout(() => (window as any).Prism.highlightAll(), 10);
				}
			} catch (error) {
				console.error('SQL generation error:', error);
				generatedSQL = `-- Error generating SQL: ${error instanceof Error ? error.message : 'Unknown error'}\n`;
				generatedSQL += `-- Please ensure your model is valid and try again.\n`;
			} finally {
				isGenerating = false;
			}
		}, 0);
	}

	function copyToClipboard() {
		navigator.clipboard.writeText(generatedSQL);
		copied = true;
		setTimeout(() => {
			copied = false;
		}, 2000);
	}

	function downloadSQL() {
		const blob = new Blob([generatedSQL], { type: 'text/plain' });
		const url = URL.createObjectURL(blob);
		const a = document.createElement('a');
		a.href = url;
		a.download = `${projectName.replace(/\s+/g, '_')}_schema.sql`;
		a.click();
		URL.revokeObjectURL(url);
	}
</script>

<LoadingScreen {isLoading} />

<div class="export-page">
	<div class="header">
