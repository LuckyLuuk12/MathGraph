<script lang="ts">
	import { page } from '$app/stores';
	import { onMount } from 'svelte';
	import { goto } from '$app/navigation';
	import { browser } from '$app/environment';
	import { projectStore } from '$lib/stores/project-store';
	import { canvasStore } from '$lib/stores/canvas-store';
	import type { Project } from '$lib/stores/project-store';
	import { SQLDialect } from '$lib/constants';
	import LoadingScreen from '$lib/components/LoadingScreen.svelte';

	let projectId = $state('');
	let project: Project | null = null;
	let projectName = $state('');
	let selectedDialect = $state<SQLDialect>(SQLDialect.PostgreSQL);
	let generatedSQL = $state('');
	let copied = $state(false);
	let isLoading = $state(true);

	onMount(() => {
		projectId = $page.params.id || '';
		if (projectId) {
			loadProject();
		} else {
			goto('/projects');
		}
	});

	// Regenerate SQL when dialect changes
	$effect(() => {
		if (project && selectedDialect) {
			generateSQL();
		}
	});

	function loadProject() {
		isLoading = true;
		try {
			const allProjects = projectStore.getAllProjects();
			project = allProjects.find((p) => p.id === projectId) || null;

			if (project) {
				projectName = project.name;
				canvasStore.loadState(project.canvasState);
				generateSQL();
				// Delay to ensure everything is loaded
				setTimeout(() => {
					isLoading = false;
				}, 150);
			} else {
				// Project not found, redirect to projects page
				isLoading = false;
				goto('/projects');
			}
		} catch (error) {
			console.error('Failed to load project:', error);
			isLoading = false;
			goto('/projects');
		}
	}

	function generateSQL() {
		// TODO: Convert canvas state to Information Schema and generate SQL
		// For now, generate a placeholder based on nodes/edges
		const nodes = Array.from($canvasStore.nodes.values());
		const edges = Array.from($canvasStore.edges.values());

		if (!nodes || nodes.length === 0) {
			generatedSQL = '-- No entities or fact types found in the project.';
			return;
		}

		let sql = `-- ============================================================\n`;
		sql += `-- Generated by MathGraph - Visual Information Modeling Tool\n`;
		sql += `-- https://mathgraph.kablan.nl\n`;
		sql += `-- Created by Luuk Kablan\n`;
		sql += `-- ============================================================\n`;
		sql += `--\n`;
		sql += `-- Project: ${projectName}\n`;
		sql += `-- Dialect: ${selectedDialect}\n`;
		sql += `-- Generated: ${new Date().toISOString()}\n`;
		sql += `--\n`;
		sql += `-- ============================================================\n\n`;

		// Generate CREATE TABLE statements for entities
		const entities = nodes.filter((n) => n.type === 'entity');
		for (const entity of entities) {
			sql += `CREATE TABLE ${entity.label.replace(/\s+/g, '_')} (\n`;
			sql += `  id SERIAL PRIMARY KEY,\n`;
			sql += `  name VARCHAR(255) NOT NULL,\n`;
			sql += `  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP\n`;
			sql += `);\n\n`;
		}

		// Generate relationship tables for fact types
		const factTypes = nodes.filter((n) => n.type === 'factType');
		for (const factType of factTypes) {
			const relatedEdges = edges.filter(
				(e) => e.sourceNodeId === factType.id || e.targetNodeId === factType.id
			);

			if (relatedEdges.length >= 2) {
				sql += `CREATE TABLE ${factType.label.replace(/\s+/g, '_')} (\n`;
				sql += `  id SERIAL PRIMARY KEY,\n`;

				relatedEdges.forEach((edge) => {
					const targetNode =
						$canvasStore.nodes.get(edge.targetNodeId) || $canvasStore.nodes.get(edge.sourceNodeId);
					if (targetNode && targetNode.type === 'entity') {
						sql += `  ${targetNode.label.replace(/\s+/g, '_').toLowerCase()}_id INTEGER,\n`;
					}
				});

				sql += `  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP\n`;
				sql += `);\n\n`;
			}
		}

		generatedSQL = sql;

		// Apply syntax highlighting
		if (browser && (window as any).Prism) {
			setTimeout(() => (window as any).Prism.highlightAll(), 10);
		}
	}

	function copyToClipboard() {
		navigator.clipboard.writeText(generatedSQL);
		copied = true;
		setTimeout(() => {
			copied = false;
		}, 2000);
	}

	function downloadSQL() {
		const blob = new Blob([generatedSQL], { type: 'text/plain' });
		const url = URL.createObjectURL(blob);
		const a = document.createElement('a');
		a.href = url;
		a.download = `${projectName.replace(/\s+/g, '_')}_schema.sql`;
		a.click();
		URL.revokeObjectURL(url);
	}
</script>

<LoadingScreen {isLoading} />

<div class="export-page">
	<div class="header">
		<a href="/projects" class="back-btn"> ‚Üê Projects </a>
		<h1>üóÑÔ∏è Export SQL Schema</h1>
		<div class="project-info">
			<span class="project-name">{projectName}</span>
		</div>
	</div>

	<div class="content">
		<div class="sidebar">
			<div class="section">
				<h3>Schema Overview</h3>
				<div class="stats">
					<div class="stat">
						<span class="label">Entities:</span>
						<span class="value"
							>{Array.from($canvasStore.nodes.values()).filter((n) => n.type === 'entity')
								.length}</span
						>
					</div>
					<div class="stat">
						<span class="label">Fact Types:</span>
						<span class="value"
							>{Array.from($canvasStore.nodes.values()).filter((n) => n.type === 'factType')
